// Helper functions
export const getPermissionsByCategory = (category) => {
  return Object.values(permissionDefinitions).filter(
    (perm) => perm.category === category
  );
};

export const getAllCategories = () => {
  return [
    ...new Set(
      Object.values(permissionDefinitions).map((perm) => perm.category)
    ),
  ];
};

export const getPermissionByKey = (key) => {
  return permissionDefinitions[key];
};

export const getAllPermissions = () => {
  return Object.values(permissionDefinitions);
};
